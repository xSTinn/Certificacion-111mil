package com.martinstremiz.certificacioncentrocultural.modelo;


/**
 *
 * @author marti
 */
public class Curso {
    private int idCurso;
    private String nombre;
    private String duracion;
    private String cargaHoraria;
    private Aula aula;
    private int cupo;
    private boolean tieneExamenFinal;
    private Profesor profesor;
    private Tematica tematica;

    public Curso() {
    }

    public String getDuracion() {
        return duracion;
    }

    public void setDuracion(String duracion) {
        this.duracion = duracion;
    }

    public String getCargaHoraria() {
        return cargaHoraria;
    }

    public void setCargaHoraria(String cargaHoraria) {
        this.cargaHoraria = cargaHoraria;
    }

    public Aula getAula() {
        return aula;
    }

    public void setAula(Aula aula) {
        this.aula = aula;
    }

    public int getCupo() {
        return cupo;
    }

    public void setCupo(int cupo) {
        this.cupo = cupo;
    }

    public boolean isTieneExamenFinal() {
        return tieneExamenFinal;
    }

    public void setTieneExamenFinal(boolean tieneExamenFinal) {
        this.tieneExamenFinal = tieneExamenFinal;
    }

    public Profesor getProfesor() {
        return profesor;
    }

    public void setProfesor(Profesor profesor) {
        this.profesor = profesor;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public Tematica getTematica() {
        return tematica;
    }

    public void setTematica(Tematica tematica) {
        this.tematica = tematica;
    }

    public int getIdCurso() {
        return idCurso;
    }

    public void setIdCurso(int idCurso) {
        this.idCurso = idCurso;
    }
    
}
