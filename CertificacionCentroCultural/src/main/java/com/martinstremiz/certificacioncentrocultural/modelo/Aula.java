package com.martinstremiz.certificacioncentrocultural.modelo;

/**
 *
 * @author marti
 */
public class Aula {
    private int idAula;
    private int numero;

    public Aula() {
    }

    public Aula(int numero) {
        this.numero = numero;
    }

    public int getNumero() {
        return numero;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }

    @Override
    public String toString() {
        return "Aula numero: " + this.numero;
    }

    public int getIdAula() {
        return idAula;
    }

    public void setIdAula(int idAula) {
        this.idAula = idAula;
    }
    
}
